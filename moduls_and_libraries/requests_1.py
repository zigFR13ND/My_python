import requests

##  GET ‚Äî —ç—Ç–æ –∑–∞–ø—Ä–æ—Å –Ω–∞ –ø–æ–ª—É—á–µ–Ω–∏–µ –¥–∞–Ω–Ω—ã—Ö —Å —Å–µ—Ä–≤–µ—Ä–∞ (–Ω–∞–ø—Ä–∏–º–µ—Ä, –∑–∞–≥—Ä—É–∑–∫–∞ –≤–µ–±-—Å—Ç—Ä–∞–Ω–∏—Ü—ã).
#
#
#   –ü—Ä–æ—Å—Ç–æ–π GET-–∑–∞–ø—Ä–æ—Å
response = requests.get("https://httpbin.org/get")  # –û—Ç–ø—Ä–∞–≤–ª—è–µ–º GET-–∑–∞–ø—Ä–æ—Å
print(response.text)  # –í—ã–≤–æ–¥–∏–º –æ—Ç–≤–µ—Ç —Å–µ—Ä–≤–µ—Ä–∞

#   –†–∞–∑–±–æ—Ä –æ—Ç–≤–µ—Ç–∞ —Å–µ—Ä–≤–µ—Ä–∞ (JSON)
data = response.json()  # –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ–º –æ—Ç–≤–µ—Ç –≤ —Å–ª–æ–≤–∞—Ä—å
print("\nüìå –í—ã–≤–æ–¥–∏–º JSON:")
print(data)  # –í—ã–≤–æ–¥–∏–º JSON
print("\nüìå –ü–æ–ª—É—á–∞–µ–º –∑–∞–≥–æ–ª–æ–≤–∫–∏ –∑–∞–ø—Ä–æ—Å–∞:")
print(data["headers"])  # –ü–æ–ª—É—á–∞–µ–º –∑–∞–≥–æ–ª–æ–≤–∫–∏ –∑–∞–ø—Ä–æ—Å–∞

# –ú–æ–∂–Ω–æ –ø–µ—Ä–µ–¥–∞–≤–∞—Ç—å –ø–∞—Ä–∞–º–µ—Ç—Ä—ã –∑–∞–ø—Ä–æ—Å–∞ (–Ω–∞–ø—Ä–∏–º–µ—Ä, name=–ò–ª—å–Ω—É—Ä)
params = {"name": "–ò–ª—å–Ω—É—Ä", "age": 33}
response_2 = requests.get("https://httpbin.org/get", params=params)
print("\nüìå Response 2 (URL —Å –ø–∞—Ä–∞–º–µ—Ç—Ä–∞–º–∏):")
print(response_2.url)  # URL —Å –ø–∞—Ä–∞–º–µ—Ç—Ä–∞–º–∏
# –°–µ—Ä–≤–µ—Ä –ø–æ–ª—É—á–∏—Ç https://httpbin.org/get?name=–ò–ª—å–Ω—É—Ä&age=25.

data_2 = response_2.json() 
print(f"\n–ò–º—è: {data_2['args']['name']}\n–í–æ–∑—Ä–∞—Å—Ç: {data_2['args']['age']}")

#üîπ requests.get(URL) ‚Äî –æ—Ç–ø—Ä–∞–≤–ª—è–µ—Ç GET-–∑–∞–ø—Ä–æ—Å.
#üîπ response.text ‚Äî –ø–æ–ª—É—á–∞–µ—Ç –æ—Ç–≤–µ—Ç –≤ –≤–∏–¥–µ —Å—Ç—Ä–æ–∫–∏.
#üîπ response.json() ‚Äî –ø—Ä–µ–≤—Ä–∞—â–∞–µ—Ç JSON-–æ—Ç–≤–µ—Ç –≤ —Å–ª–æ–≤–∞—Ä—å.
#üîπ params={} ‚Äî –ø–µ—Ä–µ–¥–∞—ë—Ç –ø–∞—Ä–∞–º–µ—Ç—Ä—ã –∑–∞–ø—Ä–æ—Å–∞.


##  POST-–∑–∞–ø—Ä–æ—Å –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è –¥–ª—è –æ—Ç–ø—Ä–∞–≤–∫–∏ –¥–∞–Ω–Ω—ã—Ö (–Ω–∞–ø—Ä–∏–º–µ—Ä, —Ñ–æ—Ä–º—ã –Ω–∞ —Å–∞–π—Ç–µ –∏–ª–∏ –∑–∞–ø—Ä–æ—Å–∞ –∫ API).
#
# GET –ø–µ—Ä–µ–¥–∞—ë—Ç –ø–∞—Ä–∞–º–µ—Ç—Ä—ã –≤ URL (?key=value), –∞ POST –ø–µ—Ä–µ–¥–∞—ë—Ç –¥–∞–Ω–Ω—ã–µ –≤ —Ç–µ–ª–µ –∑–∞–ø—Ä–æ—Å–∞.

# POST-–∑–∞–ø—Ä–æ—Å —Å requests.post()
data_3 = {"username": "Ilnur", "password": "12345"}  # –î–∞–Ω–Ω—ã–µ –¥–ª—è –æ—Ç–ø—Ä–∞–≤–∫–∏
response_3 = requests.post("https://httpbin.org/post", data=data_3)
print("\nüìå Response 3 (data={}):")
print(response_3.json())  # –í—ã–≤–æ–¥ –æ—Ç–≤–µ—Ç–∞ —Å–µ—Ä–≤–µ—Ä–∞


#  –û—Ç–ø—Ä–∞–≤–∫–∞ JSON-–¥–∞–Ω–Ω—ã—Ö (json={})
data_4 = {"username": "Ilnur", "password": "12345"}  # JSON-–¥–∞–Ω–Ω—ã–µ
response_4 = requests.post("https://httpbin.org/post", json=data_4)
print("\nüìå Response 4 (json={}):")
print(response_4.json())  # –í—ã–≤–æ–¥ JSON-–æ—Ç–≤–µ—Ç–∞


# data={} ‚Üí –æ—Ç–ø—Ä–∞–≤–ª—è–µ—Ç –∫–∞–∫ –æ–±—ã—á–Ω—ã–µ —Ñ–æ—Ä–º—ã (x-www-form-urlencoded). –ò—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è, –µ—Å–ª–∏ —Å–µ—Ä–≤–µ—Ä –æ–∂–∏–¥–∞–µ—Ç –æ–±—ã—á–Ω—ã–µ HTML-—Ñ–æ—Ä–º—ã (–∫–∞–∫ –Ω–∞ —Å–∞–π—Ç–∞—Ö).  –î–∞–Ω–Ω—ã–µ –æ—Ç–ø—Ä–∞–≤–ª—è—é—Ç—Å—è –≤ —Ç–µ–ª–µ –∑–∞–ø—Ä–æ—Å–∞ –≤ –≤–∏–¥–µ key=value&key=value.
#  –ö–æ–≥–¥–∞ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å? –ï—Å–ª–∏ API –∏–ª–∏ —Å–∞–π—Ç –ø—Ä–∏–Ω–∏–º–∞–µ—Ç –æ–±—ã—á–Ω—ã–µ —Ñ–æ—Ä–º—ã (Content-Type: application/x-www-form-urlencoded). –ù–∞–ø—Ä–∏–º–µ—Ä, –µ—Å–ª–∏ –Ω—É–∂–Ω–æ –∞–≤—Ç–æ—Ä–∏–∑–æ–≤–∞—Ç—å—Å—è –Ω–∞ —Å–∞–π—Ç–µ —á–µ—Ä–µ–∑ requests.

# json={} ‚Üí –æ—Ç–ø—Ä–∞–≤–ª—è–µ—Ç –∫–∞–∫ JSON (application/json). –ò—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è, –µ—Å–ª–∏ —Å–µ—Ä–≤–µ—Ä –æ–∂–∏–¥–∞–µ—Ç JSON-—Ñ–æ—Ä–º–∞—Ç (Content-Type: application/json). –î–∞–Ω–Ω—ã–µ –æ—Ç–ø—Ä–∞–≤–ª—è—é—Ç—Å—è –∫–∞–∫ {"key": "value"} –≤ —Ç–µ–ª–µ –∑–∞–ø—Ä–æ—Å–∞.
#  –ö–æ–≥–¥–∞ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å? –ï—Å–ª–∏ API —Ç—Ä–µ–±—É–µ—Ç JSON (Content-Type: application/json). –ù–∞–ø—Ä–∏–º–µ—Ä, –µ—Å–ª–∏ —Ä–∞–±–æ—Ç–∞–µ—à—å —Å Telegram API, VK API, GitHub API.

#üí° –ì–ª–∞–≤–Ω–æ–µ –ø—Ä–∞–≤–∏–ª–æ:

# –ï—Å–ª–∏ —Å–µ—Ä–≤–µ—Ä –∂–¥—ë—Ç HTML-—Ñ–æ—Ä–º—É ‚Üí –∏—Å–ø–æ–ª—å–∑—É–π data={}.   "form"	–û–±—ã—á–Ω—ã–µ —Ñ–æ—Ä–º—ã (–Ω–∞–ø—Ä–∏–º–µ—Ä, –∞–≤—Ç–æ—Ä–∏–∑–∞—Ü–∏—è –Ω–∞ —Å–∞–π—Ç–µ)
# –ï—Å–ª–∏ —Å–µ—Ä–≤–µ—Ä –∂–¥—ë—Ç JSON ‚Üí –∏—Å–ø–æ–ª—å–∑—É–π json={}.          API, –∫–æ—Ç–æ—Ä—ã–µ –ø—Ä–∏–Ω–∏–º–∞—é—Ç JSON (–Ω–∞–ø—Ä–∏–º–µ—Ä, Telegram API)


